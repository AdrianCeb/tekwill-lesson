package homework_nr_4_1;

public class MathOperation {

    public int sum(int x, int y){
        return x + y;
    }

    public int subtraction(int x, int y){
        return x - y;
    }

    public float divide(int x, int y){
        return (float) x/y;
    }

    public int multiply(int x, int y){
        return x * y;

    }
}
